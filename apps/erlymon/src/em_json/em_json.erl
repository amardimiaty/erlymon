-module(em_json).
-author("Sergey Penkovsky <sergey.penkovsky@gmail.com>").

-include("em_records.hrl").
%% API
-export([
  encode/1,
  decode/1
]).

-spec(encode(Object :: map()) -> string()).
encode(Object) ->
  jsx:encode(Object).

-spec(decode(Binary :: string()) -> map()).
decode(Binary) ->
  jsx:decode(Binary, [return_maps]).
